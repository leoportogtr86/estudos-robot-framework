def soma_dois_numeros(a, b):
    """
    Esta função soma dois números inteiros.

    Parâmetros:
    a (int): Primeiro números a ser somado.
    b (int): Segundo números a ser somado.

    Retorna:
    int: Resultado da soma
    """
    # Corpo da função
    return int(a) + int(b) 

def subtracao(a, b):
    return int(a) - int(b) 

def multiplica_com_fator_opcional(a, b, fator = 1):
    return int(a) * int(b) * fator

